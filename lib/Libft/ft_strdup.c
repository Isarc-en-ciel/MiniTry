#include "libft.h"

static char	*ft_strcpy(char *dest, const char *src)
{
	int	i;

	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}

char	*ft_strdup(const char *s1)
{
	char	*str;

	str = malloc (sizeof(char) * (ft_strlen(s1) + 1));
	if (!str)
		return (0);
	str = ft_strcpy(str, s1);
	return (str);
}
